function getCourseKeyFromURL() {
  const params = new URLSearchParams(window.location.search);
  return params.get("course") || null;
}

function scrollCarousel(trackId, dir) {
  const track = document.getElementById(trackId);
  if (track) {
    track.scrollBy({ left: dir * 220, behavior: 'smooth' });
  }
}

function renderCourseCarousels(courseKey) {
  const data = courseData[courseKey];
  const container = document.getElementById("course-carousels");

  if (!data) {
    container.innerHTML = `<p style="padding:1rem;">Course not found. Check the URL parameter.</p>`;
    return;
  }

  document.title = data.title + " | DuHive";
  container.innerHTML = `
    <section class="intro">
      <h1>${data.title}</h1>
    </section>
    ${data.carousels.map(carousel => `
      <section class="docs-section">
        <h2>${carousel.heading}</h2>
        <div class="carousel">
          <button class="carousel-nav carousel-prev" onclick="scrollCarousel('${carousel.id}', -1)">‹</button>
          <div class="carousel-track" id="${carousel.id}">
            ${carousel.documents.map(doc => `
              <a href="${doc.file}" class="doc-card" style="text-decoration: none;">
                <img src="${doc.thumbnail}" alt="${doc.title}" />
                <div class="doc-card-content">
                  <h3>${doc.title}</h3>
                  <div class="meta">
                    <span>${doc.ratings}</span>
                    <!--<button class="save-btn">Save</button>-->
                  </div>
                </div>
              </a>
            `).join("")}
          </div>
          <button class="carousel-nav carousel-next" onclick="scrollCarousel('${carousel.id}', 1)">›</button>
        </div>
      </section>
    `).join("")}
  `;
}

const courseKey = getCourseKeyFromURL();
renderCourseCarousels(courseKey);


